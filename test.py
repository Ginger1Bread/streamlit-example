print("Hello, world!")  # Outputs "Hello, world!" to the console

x = 10
y = 20
print("The sum of", x, "and", y, "is", x + y)  # Outputs "The sum of 10 and 20 is 30" to the console

name = "John"
age = 25
print("My name is", name, "and I am", age, "years old.", sep="-")  # Outputs "My name is-John-and I am-25-years old." to the console with hyphens as separators

print("Hello, world!", end="")  # Outputs "Hello, world!" to the console without a newline character at the end
